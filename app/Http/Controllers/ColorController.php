    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        request()->validate([
            'hexCode' => ['required', 'unique:colors,hexCode', new Hex]
        ]);

        $color = new Color(['hexCode' => $request->hexCode]);
        $color->save();

        return response()->json('Color added', 200);

    }

